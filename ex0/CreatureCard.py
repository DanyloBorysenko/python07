from typing import Dict
from ex0.Card import Card, CardType


class CreatureCard(Card):
    @property
    def card_type(self) -> CardType:
        return CardType.CREATURE

    def __init__(self, name: str, cost: int, rarity: str,
                 attack: int, health: int) -> None:
        super().__init__(name, cost, rarity)
        if attack <= 0:
            raise ValueError("CreatureCard: attack must be positive")
        if health <= 0:
            raise ValueError("CreatureCard: health must be positive")
        self.attack = attack
        self.health = health

    def get_card_info(self) -> Dict:
        card_info: Dict = super().get_card_info()
        card_info["attack"] = self.attack
        card_info["health"] = self.health
        return card_info

    def play(self, game_state: Dict) -> Dict:
        if self.is_playable(game_state["total_mana"]):
            game_state["total_mana"] -= self.cost
            return {'card_played': self.name,
                    'mana_used': self.cost,
                    'effect': 'Creature summoned to battlefield'}
        else:
            print("Not enough mana")
            return {}

    def attack_target(self, target: "CreatureCard") -> Dict:
        return {'attacker': self.name, 'target': target.name,
                'damage_dealt': self.attack, 'combat_resolved': True}
    
    def is_playable(self, available_mana: int) -> bool:
        return available_mana >= self.cost and self.health > 0
